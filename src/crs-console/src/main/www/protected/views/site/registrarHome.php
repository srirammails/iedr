<p>
    Welcome to the IEDR console for Registrars.
    This system provides all Registrars with online domain-by-domain management capability for their portfolios of .ie domains on a 24/7/365 basis.
</p>
<p>Registrars are expected to assume full responsibility and accountability for domain renewal/non-renewal, payments and NRP suspensions and reactivations.</p>
<p>
    IEDR staff will remain available for support and for query resolution
    (<a href="mailto:accounts@iedr.ie?Subject=Feedback">accounts@iedr.ie</a>,
    <a href="mailto:registrations@iedr.ie?Subject=Feedback">registrations@iedr.ie</a>
    and <a href="mailto:asd@iedr.ie?Subject=Feedback">asd@iedr.ie</a>).
</p>
For information on new business processes (BPR) please visit <a target="_blank" href="http://why.ie/bpr/">why.ie/bpr</a>.
