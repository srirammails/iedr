<p>
    Welcome to the domain management console for direct customers.
    This system provides customers with online domain-by-domain management capability for their portfolios of .ie domains on a 24/7/365 basis.
</p>

<p>
    Customers are expected to assume full responsibility and accountability for domain renewals,
    payments and reactivation from suspensions, however IEDR staff will remain available for support and for query resolution
    (<a href="mailto:accounts@iedr.ie?Subject=Feedback">accounts@iedr.ie</a>,
    <a href="mailto:registrations@iedr.ie?Subject=Feedback">registrations@iedr.ie</a>
    and <a href="mailto:asd@iedr.ie?Subject=Feedback">asd@iedr.ie</a>).
</p>

<p>
    While we endeavored to make this Console user-friendly,
    intuitive and easy-to-use you may find our supporting documentation useful.
    For user-friendly guidelines on how to renew and pay for your domains, or reactivate suspended domains,
    please visit <a target="_blank" href="http://why.ie/bpr/">why.ie/bpr</a>. This resource has an FAQ and helpful and step-by-step procedures.
</p>
